/*
3.Да се състави програма, която извежда всички естествени трицифрени числа,
които нямат еднакви цифри т.е. 100 и 101 не се извеждат.
Програмата да изведе броят на числата
Изход: Общ брой на числата 648
*/

#include<iostream>
using namespace std;
int main()
{
    int counter = 0;
    for(int i = 100; i <= 999; i++)
    {
        if(i/100 != i/10%10 && i/10%10 != i%10 && i/100 != i%10)
        {
            counter++;
        }
    }
    cout<<counter;


return 0;
}
